# 문자열 연산자의 우선순위
print("안녕" + "하세요" * 3)
print(("안녕" + "하세요") * 3)
print("안녕" + ("하세요" * 3))

### 01. 숫자 자료형에는 소수점이 없는 정수형과 소수점이 있는 실수형이 있다.
### 02. 숫자 연산자에는 +, -, *, /와 같은 사칙연산자와 //(정수나누기 연산자), %(나머지 연산자), **(제곱 연산자)가 있다.
### 03. 연산자에는 우선순위가 존재하는데, 1) 곱하기와 나누기가 우선이고, 2) 더하기와 빼기가 다음으로 우선, 3) 잘 모를 때는 괄호를 쳐서 나타낸다.
