# 큰 따옴표로 문자열 만들기
print("안녕하세요")

# 작은 따옴표로 문자열 만들기
print('안녕하세요')

# 문자열 내부에 따옴표 넣기
# print(""안녕하세요"라고 말했습니다") #오류
print('"안녕하세요"라고 말했습니다') #정상
print("'안녕하세요'라고 말했습니다") #정상

# 이스케이프 문자를 사용해 문자열 만들기
print("\"안녕하세요\"라고 말했습니다")
print('\'안녕하세요\'라고 말했습니다')

print("안녕하세요\n라고 말했습니다")
print("안녕하세요\t라고 말했습니다")

print("\\ \\ \\ \\")

# 여러 줄 문자열 만들기
print("동해물과\n백두산이\n마르고\n닳도록")
print("""동해물과
백두산이
마르고
닳도록""") # 여러줄 문자열 기능

# 줄바꿈 없이 문자열 만들기
print("""
동해물과
백두산이
마르고
닳도록
""")
print("""\
동해물과
백두산이
마르고
닳도록\
""") # 첫째줄과 마지막줄에 의도치 않은 줄바꿈 삭제

# 문자열 연결 연산자: +
print("안녕" + "하세요")
print("안녕하세요" + "!")
print("안녕하세요" + 1) #오류

# 문자열 반복 연산자: *
print("안녕하세요" * 3)
print(3 * "안녕하세요")

# 문자 선택 연산자(인덱싱): []
print("안녕하세요"[0])
print("안녕하세요"[1])
print("안녕하세요"[2])
print("안녕하세요"[3])
print("안녕하세요"[4])

print("안녕하세요"[-1])
print("안녕하세요"[-2])
print("안녕하세요"[-3])
print("안녕하세요"[-4])
print("안녕하세요"[-5])

# 문자열 범위 선택 연산자(슬라이싱): [:]
print("안녕하세요"[1:4])
print("안녕하세요"[0:2]) #(뒤의 숫자)번째까지 선택하는 것이 아니라 (뒤의 숫자-1)번째까지 선택함을 주의할 것!!!
print("안녕하세요"[1:3])
print("안녕하세요"[2:4])

print("안녕하세요"[1:]) # 녕하세요
print("안녕하세요"[:3]) # 안녕하

hello = "안녕하세요" #문자열 선택 연산자로 슬라이싱을 하더라도 원본은 변하지 않음
print(hello[0:2])
