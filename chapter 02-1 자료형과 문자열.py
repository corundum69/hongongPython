# IndexError(Index out of range) 예외
print("안녕하세요"[10]) #오류

# 문자열 길이 구하기
print(len("안녕하세요"))

### 01. 자료의 형식을 자료형(data type)이라고 한다.
### 02. 문자의 나열을 문자열이라고 하는데, 문자열은 큰따옴표 혹은 작은따옴표로 입력한다. 
### 03. 이스케이프 문자는 문자열 내부에서 특수한 기능을 수행하는 문자열을 말한다.
### 04. 문자열 연산자에는 문자열 연결 연산자(+), 문자열 반복 연산자(*), 문자열 선택 연산자([]), 문자열 범위 선택 연사자([:])가 있다
### 05. type()은 자료형을 확인하는 함수이며, len()는 문자열의 길이를 구하는 함수이다.
